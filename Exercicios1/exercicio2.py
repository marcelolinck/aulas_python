# 2. Peça ao usuário para inserir dois números. Realize as quatro operações aritméticas básicas (adição,
# subtração, multiplicação e divisão) com esses números e imprima os resultados.

print("##MiniCalculadora##")

numero1 = float(input("informe o primeiro numero: "))
numero2 = float(input("informe o segundo numero: "))

print("A soma de ", numero1,"+",numero2," =",numero1+numero2,".")
print("A subtracao de ", numero1,"-",numero2," =",numero1-numero2,".")
print("A multiplicacao de ", numero1,"*",numero2," =",numero1*numero2,".")
print("A divisao de ", numero1,"/",numero2," =",numero1/numero2,".")
